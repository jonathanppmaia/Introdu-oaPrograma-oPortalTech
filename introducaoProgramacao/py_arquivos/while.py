i = 1
while (i <= 5):
    print(i)
    i += 1


#Perceba que utilizamos a variável “i”, que funciona como um contador e é usada para controlar a repetição do loop while. 
# Veja também que incrementamos essa variável dentro das instruções do loop. Isso faz com que a condição de avaliação encontre o ponto de parada e encerre o loop.
#Vale ressaltar que é fundamental indicarmos o ponto de parada na instrução while.Caso contrário,teremos uma falha que faz com que o programa fique em “loop infinito”, ou seja, repetindo o bloco de código sem parar. 
